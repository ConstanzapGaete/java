        lista=omega.filtroT(txtT.getText());
        if(lista.isEmpty()){
            JOptionPane.showMessageDialog(null, "La pelicula no existe"); 
        }else{
            String titulo=txtT.getText();
        int confi=JOptionPane.showConfirmDialog(null,"Estas seguro de eliminar : "+titulo,"ELIMINAR PELICULA",JOptionPane.YES_NO_OPTION);
        if (confi== JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, "La pelicula "+titulo+" fue Eliminada");
            db.Eliminar(titulo);
            txtT.setText(" ");
        }else{
            txtT.setText(" ");
        }
       
        }

