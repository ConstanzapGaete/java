    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed

      if (txtT.getText().isEmpty()||txtDir.getText().isEmpty()||txtA.getText().isEmpty()||txtDur.getText().isEmpty()){
          JOptionPane.showMessageDialog(null,"Debes rellenar todas las casillas");
     }else{
        String titulo=txtT.getText();
        String Director=txtDir.getText();
        int ano=Integer.parseInt(txtA.getText());
        int duracion=Integer.parseInt(txtDur.getText());
        String genero=(String) combo.getSelectedItem();
        db.actualizar(titulo, Director, ano, duracion, genero);
        JOptionPane.showMessageDialog(null, titulo+" Modificada");
        txtT.setText("");
        txtA.setText("");
        txtDir.setText("");
        txtDur.setText("");
        combo.setSelectedIndex(0);
     }
        
     
    }//GEN-LAST:event_btBuscarActionPerformed

