    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       List<peliculasDTO> lista = new ArrayList<>();
       lista=omega.filtroT(txtT.getText());
       if(lista.isEmpty()){
           JOptionPane.showMessageDialog(null, "La pelicula no existe");
       }else{
            peliculasDTO beta=lista.get(0);
            peliculasDTO alfa =new peliculasDTO();
            alfa.setDirector(beta.getDirector());
            alfa.setAno(beta.getAno());
            alfa.setDuracion(beta.getDuracion());
            alfa.setGenero(beta.getGenero());
            txtDur.setText(String.valueOf(beta.getDuracion()));
            txtDir.setText(beta.getDirector());
            txtA.setText(String.valueOf(beta.getAno()));
            combo.setSelectedItem(beta.getGenero());
           
       }
       
      
      
       
       
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

